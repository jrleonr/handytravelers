Has estado en ciudades distintas y no has hecho ningún amigo. Paseas cada día por un lugar diferente y, sin embargo, acaba siendo la misma historia.

Te ha pasado. Y a mi tambien.

Incluso grandes viajeros acaban atrapados en las redes del turismo sin saber cómo.

Si eres como yo, sabes que lo mejor de los lugares no son ni los monumentos ni las atracciones turísticas.

**Son las personas.**

En mi último viaje haciendo autostop de Londres a Estambul descubrí lo generosas que son las personas, incluso con desconocidos. Me dieron mucho más que transporte.

Me abrieron las puertas de su vida.

Pero sentí que me faltó mucho por conocer durante mi aventura. Cuando llegaba a mi destino, era como estar en el mismo sitio. Deambulaba por las calles sintiendo que me estaba perdiendo la verdadera esencia. Entonces, decidí intentar diferentes webs de viajes.

Enviar una request personalizada en Couchsurfing era tarea imposible. No tenía tiempo. Llegaba tarde a los hostales y sólo podía escribir algunas request que casi nunca tenían respuesta.

Probé AirBnB, anunciaban inmersión en la cultura, vivir como un local… A precios imposibles. No me lo podía permitir.

Buscaba historias que contar. Mi viaje por carretera era real, lleno de aventuras. Pero mis horas en las ciudades eran huecos vacíos en mi viaje y sentía que me estaba dejando algo.

Entonces, pensé, ¿por qué no puedo buscar alojamiento de la misma forma que hago autostop?

Y cree Handytravelers.

